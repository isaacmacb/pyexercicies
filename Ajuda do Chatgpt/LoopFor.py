# Loop for com range / range é usada para um sequencia de números

for i in range(3):
    print(f"Iteration {i}")
#As f-strings permitem incluir expressões dentro de chaves {} 
# Output: Iteration 0, Iteration 1, Iteration 2

# Iterando sobre uma lista
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(f"I like {fruit}")
    print("I like" + fruit)
#As f-strings permitem incluir expressões dentro de chaves {} 
# Output: I like apple, I like banana, I like cherry
