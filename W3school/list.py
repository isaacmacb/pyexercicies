"""Imprima o segundo item da fruitslista."""

fruits = ["apple", "banana", "cherry"]
print(fruits[1])

"""Altere o valor de "apple" para "kiwi" na fruitslista."""

fruits = ["apple", "banana", "cherry"]
fruits[1] = "Kiwi"

"""Use o appendmétodo para adicionar "laranja" à fruitslista."""

fruits = ["apple", "banana", "cherry"]
fruits.append["Orange"]

"""Use o remove para tirar um item de lista"""
fruits = ["apple", "banana", "cherry"]
fruits.remove("banana")

"""Use um intervalo de índices para imprimir o terceiro, quarto e quinto item na lista."""
fruits = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(fruits[2:5])